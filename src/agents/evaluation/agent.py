"""
Evaluation Agent

Evaluates outputs generated by other agents.
"""

from src.core.base_agent import BaseAgent
from .skills import basic_score


class EvaluationAgent(BaseAgent):
    """Simple evaluation agent for the MVP."""

    def __init__(self):
        super().__init__("evaluation")

    def execute(self, task: str):
        score = basic_score(task)

        return {
            "agent": self.name,
            "task": task,
            "score": score,
            "status": "PASS" if score >= 0.7 else "REVIEW",
        }