"""
Evaluation Agent Module

Evaluates outputs generated by other agents.
"""
from typing import Any, Dict
import logging

from src.core.base_agent import BaseAgent
from .skills import basic_score

logger = logging.getLogger(__name__)


class EvaluationAgent(BaseAgent):
    """Simple evaluation agent for the MVP."""

    def __init__(self) -> None:
        """Initialize the evaluation agent."""
        super().__init__("evaluation")

    def execute(self, task: str) -> Dict[str, Any]:
        """
        Evaluate a given task output.

        Args:
            task (str): The output content to evaluate.

        Returns:
            Dict[str, Any]: The evaluation result including score and status.
        """
        logger.info("Evaluation agent executing scoring.")
        score = basic_score(task)

        return {
            "agent": self.name,
            "task": task,
            "score": score,
            "status": "PASS" if score >= 0.7 else "REVIEW",
        }